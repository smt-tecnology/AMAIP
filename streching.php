                                        <p class="parrafo-titulo"> 
                                            Stretching, es una palabra en inglés que significa EXTENSIÓN y una forma de ejercicio físico que propicia el estiramiento de la musculatura que recubre nuestro esqueleto. 
                                        </p>
                                        <p class="parrafo-titulo">
                                            La elongación es uno de los métodos más usados para devolverle al sistema muscular su capacidad de trabajo- flexibilidad y contractilidad- aliviar tensiones en ligamentos, tendones y articulaciones.
                                        </p>
                                        <p class="parrafo-titulo">
                                            La diferencia entre estirar y elongar se podría reducir en simples palabras a que estirar se asemeja a desperezarse.
                                        </p>
                                        <p class="parrafo-titulo">
                                            Es importante conocer la respuesta del organismo ante el estiramiento, que es lo que estimulamos cuando estiramos y que beneficios se obtienen, para integrar esta técnica en los diferentes programas de movimiento y entrenamiento.
